# ============================================
# EXPRESSIONS VS STATEMENTS
# ============================================

# --------------------------------------------
# WHAT IS AN EXPRESSION?
# --------------------------------------------

# An expression produces a value (a result).
# When Python runs it, you get something back.

# Examples:
2 + 3           # This gives you 5
"Hello" + " World"  # This gives you "Hello World"
10 * 5          # This gives you 50
len("Python")   # This gives you 6 (the length of the word)

# Think of expressions like questions with answers:
# "What is 2 + 3?" → Answer: 5
# "What is 10 * 5?" → Answer: 50

# --------------------------------------------
# WHAT IS A STATEMENT?
# --------------------------------------------

# A statement does something (performs an action).
# It doesn't always give you a value back.

# Examples:
x = 10          # This stores 10 in a box called x
print("Hello")  # This displays "Hello" on the screen

# Think of statements like commands:
# "Store this value!"
# "Display this message!"

# --------------------------------------------
# EXPRESSIONS VS STATEMENTS: THE KEY DIFFERENCE
# --------------------------------------------

# Expression = Produces a result
# Statement = Performs an action

# Here's an example that uses both:
x = 2 + 3  # "2 + 3" is an expression (produces 5)
# "x = 2 + 3" is a statement (stores the result)

# Another example:
print(5 * 4)  # "5 * 4" is an expression (produces 20)
# "print(5 * 4)" is a statement (displays the result)

# --------------------------------------------
# REAL-WORLD EXAMPLES
# --------------------------------------------

# Example 1: Calculate and store
age = 25  # Statement: stores 25 in age
next_year = age + 1  # Expression: age + 1 produces 26
# Statement: stores 26 in next_year

# Example 2: Calculate and display
total = 100 + 50  # Statement: stores result
print(total)      # Statement: displays the result

# Example 3: Check string length
name = "Alice"          # Statement
name_length = len(name)  # Expression: len(name) produces 5
print(name_length)      # Statement: displays 5

