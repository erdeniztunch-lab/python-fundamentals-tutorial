# ============================================
# WHAT IS PYTHON?
# ============================================

# Python is a programming language.
# Think of it like learning a new language to talk to computers.
# Instead of Spanish or French, you're learning Python.

# --------------------------------------------
# HOW DOES PYTHON WORK?
# --------------------------------------------

# 1. You write instructions (code) in a file
# 2. Python reads your instructions line by line from top to bottom
# 3. Python does exactly what you tell it to do

# For example:
print("Hello!")  # This tells Python to display "Hello!" on the screen

# --------------------------------------------
# WHAT CAN YOU DO WITH PYTHON?
# --------------------------------------------

# - Build websites
# - Analyze data
# - Create games
# - Automate boring tasks
# - Build apps
# - And much more!

# --------------------------------------------
# HOW TO RUN PYTHON CODE
# --------------------------------------------

# Method 1: Using the Terminal (Command Line)
# ----------------------------------------
# The terminal is a window where you type commands to tell your computer what to do.
# It looks like a black or white window with text.

# To run a Python file:
# 1. Open your terminal
# 2. Navigate to where your file is saved
# 3. Type: python filename.py
# 4. Press Enter

# Example:
# python 01_what_is_python.py

# Method 2: Using an IDE (Like VS Code, PyCharm)
# --------------------------------------------
# An IDE is a fancy text editor made for writing code.
# It usually has a "Run" button you can click.

